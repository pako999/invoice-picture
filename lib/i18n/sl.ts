function pluralDays(n: number, lang: "sl" | "en"): string {
  if (lang === "en") return n === 1 ? "day" : "days";
  // Slovenian: 1 → "dan", 2 → "dni" (modern usage), 3-4 → "dni", 5+ → "dni"
  if (n === 1) return "dan";
  return "dni";
}

